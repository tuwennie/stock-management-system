    private void tabloyuDoldur(List<Urun> urunListesi) {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Tabloyu temizle

    // Sütun başlıklarını kontrol et (Gerekiyorsa ekle)
    if (model.getColumnCount() == 0) {
        model.addColumn("ID");
        model.addColumn("Ürün Adı");
        model.addColumn("Fiyat");
        model.addColumn("Stok");
        model.addColumn("Kategori");
    }

    // Listeden gelen verileri tabloya ekle
    for (Urun urun : urunListesi) {
        Object[] row = {
            urun.getUrunId(),
            urun.getUrunAdi(),
            urun.getFiyat(),
            urun.getStok(),
            urun.getKategori()
        };
        model.addRow(row);
    }
}

