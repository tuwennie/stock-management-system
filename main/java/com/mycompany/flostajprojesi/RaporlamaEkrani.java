    private void RaporOlusturActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RaporOlusturActionPerformed
        try {
        // Rapor türünü JComboBox'tan al
        String seciliRapor = cmbRaporTuru.getSelectedItem().toString(); 
        UrunService urunService = new UrunService();
        List<Urun> raporSonuclari = null;

        if (seciliRapor.equals("Tüm Stok Raporu")) {
            // Tüm ürünleri çek
            raporSonuclari = urunService.tumUrunleriGetir();
            
        } else if (seciliRapor.equals("Stok Uyarısı (Az Kalanlar)")) {
            // Az stoklu ürünleri çekecek yeni bir metot çağırılacak
            raporSonuclari = urunService.dusukStokluUrunleriGetir(); 
            
        } else {
            // Hiçbir rapor seçilmediyse
            hataGoster("Lütfen bir rapor türü seçin.");
            return; 
        }

        // Sonuçları tabloya doldur
        if (raporSonuclari != null) {
            tabloyuDoldur(raporSonuclari);
        }
        
    } catch (Exception e) {
        hataGoster("Rapor oluşturulurken bir hata oluştu: " + e.getMessage());
    }
    }//GEN-LAST:event_RaporOlusturActionPerformed

